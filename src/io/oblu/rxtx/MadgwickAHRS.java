//---------------------------------------------------------------------------------------------------
// Variable definitions

    float beta = betaDef;                                // 2 * proportional gain (Kp)
    float q0 = 1.0f, q1 = 0.0f, q2 = 0.0f, q3 = 0.0f, q4 = 0.0f;    // quaternion of sensor frame relative to auxiliary frame
    float[] quaternion = new float[4];

